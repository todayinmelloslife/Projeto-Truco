/**
 * Uma classe para gerenciar um baralho de 48 cartas, incluindo embaralhamento e distribuição.
 * 
 * O baralho é representado usando um ArrayList de cartas. Cada carta tem um naipe e um valor.
 * 
 * Métodos:
 * - `add`: Adiciona cartas ao baralho usando um ArrayList.
 * - `embaralhar`: Embaralha o baralho.
 * - `darCarta`: Distribui uma carta do baralho.
 * 
 * O baralho é inicializado com loops aninhados:
 * - O loop externo itera sobre os naipes.
 * - O loop interno itera sobre os valores.
 * - Cada combinação de naipe e valor é adicionada ao baralho como uma nova carta.
 */